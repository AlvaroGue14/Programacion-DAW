public class bucles {
    
    public static void main(String[] args) {
        //Caso basico de bucle
        for (int i=0;i<5;i++){
            System.out.println(i);
        }
    
        //Caso bicle infinito
        int j=0;
        while (j<5){
            System.out.println(j);
            
        }

    }
}
